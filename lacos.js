// código lacos.js

console.log("Laço for:");

for (let i = 1; i <= 5; i++) {
  console.log("Valor de i:", i);
}

console.log("Laço while:");
let j = 1;
while (j <= 5) {
  console.log("Valor de j:", j);
  j++;
}

console.log("Laço do...while:");
let k = 5;
do {
  console.log("Valor de k:", k);
  k--;
} while (k >= 1);